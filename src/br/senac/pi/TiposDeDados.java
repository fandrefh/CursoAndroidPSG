package br.senac.pi;

public class TiposDeDados {
	
	public static void main(String[] args) {
		//Numerico inteiro
		//Poderia ser long ou Integer
		int inteiro = 1;
		//Numerico real
		double real = 1.5;
		//Strings (cadeia de caracteres)
		String palavra = "Mamãe";
		//Lógicos
		boolean verdadeiro = true;
		boolean falso = false;
		//Caractere, uma letra, mas assume um inteiro
		//pois é a representação interna. 
		char letra = 13;
	}

}
